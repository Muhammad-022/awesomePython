# Day 43: What is OOP, Class, and Object

# Example 1: Simple class and object
class Student:
    pass

s1 = Student()

print("Object created:", s1)


# Example 2: Class with attributes
class Car:
    brand = "Toyota"
    color = "White"

car1 = Car()

print("Brand:", car1.brand)
print("Color:", car1.color)
