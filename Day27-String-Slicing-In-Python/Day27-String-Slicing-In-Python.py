# Day 27: String Slicing in Python

text = "PythonProgramming"

# Basic slicing
print(text[0:6])      # Python
print(text[6:])       # Programming
print(text[:6])       # Python

# Slicing between indexes
print(text[2:8])      # thonPr
print(text[1:4])      # yth

# Negative slicing
print(text[-5:])      # ming
print(text[-11:-5])   # Program

# Full string using slicing
print(text[:])        # PythonProgramming
